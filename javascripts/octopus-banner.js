  // Shuffle helper
  function shuffle(arr) {
    for (var i = arr.length - 1; i > 0; i--) {
      var j = Math.floor(Math.random() * (i + 1));
      var tmp = arr[i]; arr[i] = arr[j]; arr[j] = tmp;
    }
    return arr;
  }

  // ── Build combined quote pool ──
  function buildPool() {
    var pool = [];
    var sources = [
      [SASSY, "sassy"],
      [SUPPORTIVE, "supportive"],
      [ANGRY, "angry"],
      [CONSPIRATORIAL, "conspiratorial"],
      [SAD, "sad"],
      [CHAOTIC, "chaotic"],
      [HUNGRY, "hungry"],
      [TIRED, "tired"],
      [SLAPHAPPY, "slaphappy"],
      [LAZY, "lazy"],
      [FAT, "fat"],
      [CHILL, "chill"],
      [CREEPY, "creepy"],
      [EXCITED, "excited"],
      [NOSTALGIC, "nostalgic"],
      [HOMESICK, "homesick"]
    ];
    sources.forEach(function (pair) {
      shuffle(pair[0].slice()).forEach(function (q) {
        pool.push({ text: q, mode: pair[1] });
      });
    });
    return shuffle(pool);
  }

  // ── Initialize banner ──
  function init() {
    var container = document.getElementById("octopus-banner");
    if (!container) return;

    // Build DOM
    container.innerHTML =
      '<div class="octo-banner-inner">' +
        '<canvas class="octo-banner-canvas" aria-hidden="true"></canvas>' +
        '<div class="octo-banner-text">' +
          '<span class="octo-banner-mode">~ SASSY OCTOPUS ~</span>' +
          '<span class="octo-banner-quote"></span>' +
        '</div>' +
        '<canvas class="octo-banner-canvas octo-banner-canvas-right" aria-hidden="true"></canvas>' +
      '</div>';

    var canvases = container.querySelectorAll(".octo-banner-canvas");
    var modeEl = container.querySelector(".octo-banner-mode");
    var quoteEl = container.querySelector(".octo-banner-quote");

    // Pick scale based on viewport
    var scale = window.innerWidth < 600 ? 1 : 2;
    drawOctopus(canvases[0], scale);
    drawOctopus(canvases[1], scale);
    // Flip the right one
    canvases[1].style.transform = "scaleX(-1)";

    // Quote cycling
    var pool = buildPool();
    var idx = 0;
    var charIdx = 0;
    var current = pool[0];
    var typing = true;
    var pauseFrames = 0;

    function updateMode(mode) {
      var labels = {
        sassy:           "~ SASSY OCTOPUS ~",
        supportive:      "~ SUPPORTIVE OCTOPUS ~",
        angry:           "~ ANGRY OCTOPUS ~",
        conspiratorial:  "~ CONSPIRATORIAL OCTOPUS ~",
        sad:             "~ SAD OCTOPUS ~",
        chaotic:         "~ CHAOTIC OCTOPUS ~",
        hungry:          "~ HUNGRY OCTOPUS ~",
        tired:           "~ TIRED OCTOPUS ~",
        slaphappy:       "~ SLAP HAPPY OCTOPUS ~",
        lazy:            "~ LAZY OCTOPUS ~",
        fat:             "~ FAT OCTOPUS ~",
        chill:           "~ CHILL OCTOPUS ~",
        creepy:           "~ CREEPY OCTOPUS ~",
        excited:         "~ EXCITED OCTOPUS ~",
        nostalgic:       "~ NOSTALGIC OCTOPUS ~",
        homesick:        "~ HOMESICK OCTOPUS ~"
      };
      modeEl.textContent = labels[mode] || labels.sassy;
      modeEl.className = "octo-banner-mode octo-mode-" + mode;
    }

    function tick() {
      if (pauseFrames > 0) {
        pauseFrames--;
        return;
      }

      if (typing) {
        charIdx++;
        quoteEl.textContent = current.text.substring(0, charIdx);
        if (charIdx >= current.text.length) {
          typing = false;
          pauseFrames = 60; // ~3s pause at full quote
        }
      } else {
        // Move to next quote
        idx = (idx + 1) % pool.length;
        if (idx === 0) pool = buildPool(); // reshuffle when exhausted
        current = pool[idx];
        charIdx = 0;
        typing = true;
        updateMode(current.mode);
        quoteEl.textContent = "";
      }
    }

    updateMode(current.mode);
    setInterval(tick, 50);
  }

  // Run on DOM ready
  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", init);
  } else {
    init();
  }
