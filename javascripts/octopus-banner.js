  // Eye sockets (white circles), pupils (black dots), highlights (white sparkle)
  function circlePixels(cx, cy, rSq) {
    var r = Math.ceil(Math.sqrt(rSq));
    var pts = [];
    for (var dy = -r; dy <= r; dy++)
      for (var dx = -r; dx <= r; dx++)
        if (dx * dx + dy * dy <= rSq)
          pts.push([cx + dx, cy + dy]);
    return pts;
  }

